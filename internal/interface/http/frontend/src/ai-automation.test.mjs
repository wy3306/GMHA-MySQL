test('AI approval modal never submits a click event as the plan', () => {
  assert.match(source, /const plan = planInput\?\.id \? planInput : approvalPlan\.value/)
  assert.match(source, /@click="executePlan\(\)"/)
  assert.doesNotMatch(source, /@click="executePlan"/)
  assert.match(source, /const message = err\.message\s+await load\(true\)\s+if \(approvalPlan\.value\?\.id === plan\.id\) approvalPlan\.value = null\s+resetApprovalInputs\(\)\s+error\.value = message/)
  assert.match(source, /有效至 \{\{ shortDate\(approvalPlan\.expires_at\) \}\}/)
})

test('AI workflow progress, checkpoints, pause and recovery stay beside chat', () => {
  assert.match(source, /workflows: \[\]/)
  assert.match(source, /const activeChatWorkflow = computed/)
  assert.match(source, /任务编排与实时状态/)
  assert.match(source, /activeChatWorkflow\.operations/)
  assert.match(source, /operation\.depends_on\?\.length \? '等待前置步骤' : '等待执行'/)
  assert.match(source, /最近检查点/)
  assert.match(source, /\/workflows\/pause/)
  assert.match(source, /\/workflows\/resume/)
  assert.match(source, /暂停只阻止后续步骤/)
  assert.match(source, /chatPlanID\.value = plan\.id/)
  assert.match(styles, /\.ai-runtime-workflow/)
  assert.match(styles, /\.ai-workflow-checkpoints/)
})
