import assert from 'node:assert/strict'
import { readFileSync } from 'node:fs'
import test from 'node:test'

const source = readFileSync(new URL('./ai-automation.js', import.meta.url), 'utf8')
const styles = readFileSync(new URL('./ai-automation.css', import.meta.url), 'utf8')

test('AI prompt sends on Enter while preserving IME and multiline input', () => {
  assert.match(source, /@keydown="handlePromptKeydown"/)
  assert.match(source, /event\.key !== 'Enter' \|\| event\.shiftKey \|\| event\.isComposing/)
  assert.match(source, /event\.preventDefault\(\)\s+sendPrompt\(\)/)
  assert.match(source, /chatStream\.value\.scrollTop = chatStream\.value\.scrollHeight/)
  assert.match(source, /pendingUserMessages\.value\.push/)
  assert.match(source, /正在发送/)
  assert.match(source, /Enter 发送/)
  assert.match(source, /Shift \+ Enter 换行/)
})

